            int id = Integer.parseInt(idStr);

            out.println("<!DOCTYPE html>");
            out.println("<html>");
            out.println("<head>");
            out.println("<title>Servlet Delete</title>");
            out.println("</head>");
            out.println("<body>");
            
            boolean deleted = false;
            brukerDAO = new BrukerDAO();
            deleted = brukerDAO.delete(id);
                
